
public class Student {
	String names;
	int scores;
	
	public Student(String name, int score) {
		this.names = name;
		this.scores = score;
	}
	
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public int getScores() {
		return scores;
	}
	public void setScores(int scores) {
		this.scores = scores;
	}
}
